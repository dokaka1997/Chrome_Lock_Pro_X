function formatProfilePolicies(blurState) {
  const policies = [];

  if (blurState?.profileForceLock) {
    policies.push(t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x66\x6f\x72\x63\x65\x5f\x6c\x6f\x63\x6b'));
  }
  if (blurState?.profileReAuthOnVisit) {
    policies.push(t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x72\x65\x61\x75\x74\x68'));
  }
  if (blurState?.profileBypassGlobalLock) {
    policies.push(t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x62\x79\x70\x61\x73\x73'));
  }
  if ((blurState?.profileForceLock || blurState?.profileReAuthOnVisit) && typeof blurState?.profilePageUnlockMinutes === '\x6e\x75\x6d\x62\x65\x72') {
    policies.push(
      blurState.profilePageUnlockMinutes > 0
        ? t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x70\x61\x67\x65\x5f\x75\x6e\x6c\x6f\x63\x6b', {
            minutes: formatProfileMinutes(blurState.profilePageUnlockMinutes)
          })
        : t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x70\x61\x67\x65\x5f\x75\x6e\x6c\x6f\x63\x6b\x5f\x69\x6e\x64\x65\x66\x69\x6e\x69\x74\x65')
    );
  }
  if (typeof blurState?.effectiveSessionMinutes === '\x6e\x75\x6d\x62\x65\x72') {
    policies.push(t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x64\x65\x66\x61\x75\x6c\x74\x5f\x73\x65\x73\x73\x69\x6f\x6e', {
      minutes: formatProfileMinutes(blurState.effectiveSessionMinutes)
    }));
  }
  if (blurState?.titleMasked) {
    policies.push(t('\x70\x6f\x70\x75\x70\x2e\x74\x6f\x6f\x6c\x2e\x70\x72\x6f\x66\x69\x6c\x65\x5f\x66\x6c\x61\x67\x2e\x6d\x61\x73\x6b\x65\x64'));
  }

  return policies.join('\x2c\x20');
}

