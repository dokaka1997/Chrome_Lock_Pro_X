async function getActiveTab() {
  const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });
  return tab || null;
}

async function sendMessageToActiveTab(message) {
  const tab = await getActiveTab();
  if (!tab?.id) {
    return { ok: false, error: t('\x70\x6f\x70\x75\x70\x2e\x65\x72\x72\x6f\x72\x2e\x6e\x6f\x5f\x74\x61\x62') };
  }

  try {
    const response = await chrome.tabs.sendMessage(tab.id, message);
    return response || { ok: false, error: t('\x70\x6f\x70\x75\x70\x2e\x65\x72\x72\x6f\x72\x2e\x6e\x6f\x5f\x72\x65\x73\x70\x6f\x6e\x73\x65') };
  } catch {
    return {
      ok: false,
      error: t('\x70\x6f\x70\x75\x70\x2e\x65\x72\x72\x6f\x72\x2e\x75\x6e\x73\x75\x70\x70\x6f\x72\x74\x65\x64\x5f\x70\x61\x67\x65')
    };
  }
}

